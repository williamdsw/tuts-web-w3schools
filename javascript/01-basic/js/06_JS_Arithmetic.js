
/* ===== EXEMPLO DE OPERADORES ARITMETICOS ===== */

function ArithmeticOperators ()
{
    /* Exemplos */
    var soma = 10 + 34;
    var subtracao = 40 - 7;
    var multiplicacao = 5 * 23;
    var expoente = 2 ** 3;
    var divisao = 20 / 15;
    var modulus = 15 % 3;
    var incremento = 5;
    incremento++;
    var decremento = 100;
    decremento--;

    /* Printando */
    console.log ("10 +  34 = " + soma);
    console.log ("40 - 7 = " + subtracao);
    console.log ("5 * 23 = " + multiplicacao);
    console.log ("2 ** 3 = " + expoente);
    console.log ("20 / 15 = " + divisao);
    console.log ("15 % 3 = " + modulus);
    console.log ("5++ = " + incremento);
    console.log ("100-- = " + decremento);
}

ArithmeticOperators ();